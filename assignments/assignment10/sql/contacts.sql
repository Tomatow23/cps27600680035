CREATE TABLE contacts{
    id int NOT NULL AUTO_INCREMENT,
    name char(250) NOT NULL,
    address char(250) NOT NULL,
    city char(250) NOT NULL,
    phone char(250) NOT NULL,
    email char(250) NOT NULL,
    DOB char(250) NOT NULL,
    contact address char(250) NOT NULL,
    age char(250) NOT NULL,
    state char(250) NOT NULL,
    PRIMARY KEY(id)
}ENGINE=InnoDB